    private void load_table() {
        // Membuat tampilan model tabel
        // Membuat tampilan model tabel
        DefaultTableModel model = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int column) {
                // Semua sel tidak bisa diedit langsung
                return false;
            }
        };

        // Menambahkan kolom ke tabel
        model.addColumn("No");
        model.addColumn("ID Karyawan");
        model.addColumn("Nama");
        model.addColumn("No. Telepon");
        model.addColumn("Alamat");

        // Hapus data lama dari tabel
        jKaryawan.setModel(model);

        // Menampilkan data dari database ke dalam tabel
        try (java.sql.Connection conn = Config.configDB(); java.sql.Statement stm = conn.createStatement(); java.sql.ResultSet res = stm.executeQuery("SELECT id_karyawan, nama, no_telp, alamat FROM t_karyawan")) {

            int no = 1; // Nomor urut
            while (res.next()) {
                model.addRow(new Object[]{
                    no++, // No urut
                    res.getString("id_karyawan"), // ID Karyawan
                    res.getString("nama"), // Nama
                    res.getString("no_telp"), // No Telepon
                    res.getString("alamat") // Alamat
                });
            }

            // Jika data kosong, beri informasi ke pengguna
            if (model.getRowCount() == 0) {
                JOptionPane.showMessageDialog(this, "Data karyawan tidak tersedia.", "Informasi", JOptionPane.INFORMATION_MESSAGE);
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Data tidak dapat ditampilkan: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

    

