    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        int selectedRow = jKaryawan.getSelectedRow(); // Mendapatkan baris yang dipilih di JTable

        // Cek jika tidak ada baris yang dipilih
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Pilih karyawan yang ingin dihapus", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Ambil ID dari kolom yang sesuai (misalnya kolom ID Karyawan adalah indeks 0)
        String id = jKaryawan.getValueAt(selectedRow, 0).toString();

        // Tampilkan konfirmasi sebelum menghapus
        int confirm = JOptionPane.showConfirmDialog(this, "Yakin ingin menghapus data ini?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
            try (java.sql.Connection conn = Config.configDB()) {
                // Query untuk menghapus data karyawan berdasarkan ID
                String sql = "DELETE FROM t_karyawan WHERE id_karyawan = ?";
                var pst = conn.prepareStatement(sql);
                pst.setString(1, id); // Set ID karyawan pada parameter query
                pst.executeUpdate(); // Eksekusi query

                // Pesan konfirmasi penghapusan berhasil
                JOptionPane.showMessageDialog(this, "Data berhasil dihapus!");

                // Muat ulang tabel untuk menampilkan data terbaru
                load_table();
            } catch (SQLException ex) {
                ex.printStackTrace();
                JOptionPane.showMessageDialog(this, "Gagal menghapus data: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnHapusActionPerformed

